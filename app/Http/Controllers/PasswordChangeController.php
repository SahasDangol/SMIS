     * @throws \Illuminate\Validation\ValidationException
     */
    public function update(Request $request, $id)
    {


        $old=$request->opass;

        $hash = User::find($id);

        if(Hash::check($old, $hash->password))
        {
            $this->validate($request, [
                'new_password' => 'required|same:confirm_password',
            ]);

            $new=Hash::make($request->new_password);
            $a = User::where('id', $id)->update(['password'=>$new]);
            Session::flash('type','success');
            Session::flash('message','Your Password has been Changed Sucessfully...');
            return redirect()->back();
        }
        else
        {
            Session::flash('type','danger');
            Session::flash('message','Old password is not matched in our record. Please Check once...');
            return redirect()->back();
        }
    }

